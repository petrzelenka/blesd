pub fn find_manufacturer(manufacturer_data: HashMap<u16, Vec<u8>>) -> Option<String> {
	if manufacturer_data.is_empty() {
		return None;
	}

	let manufacturer_id = manufacturer_data.keys().next().unwrap();
	MANUFACTURERS.get(manufacturer_id)
		.map(|manufacturer| manufacturer.to_string())
}
